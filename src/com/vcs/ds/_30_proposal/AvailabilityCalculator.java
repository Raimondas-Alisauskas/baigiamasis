package com.vcs.ds._30_proposal;

public class AvailabilityCalculator {

   private double processingTime;
    private double productionTime;

     private double processingTime (double processingSpeed, double volume) {

         return processingTime = processingSpeed * volume;
     }

    double productionTime ( double programingTime , double processingTime) {

        return productionTime = programingTime + processingTime;
    }



 }
